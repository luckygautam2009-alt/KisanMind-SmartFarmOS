export type TranslationKey = keyof typeof translations.en;

type LangCode = keyof typeof translations;

const INDO_FALLBACK: LangCode[] = ['mr', 'gu', 'te', 'ta', 'bn', 'pa', 'kn', 'ml'];

/** Resolve merged strings for a locale (Hindi gap-fill for regional languages). */
export function resolveTranslations(lang: string): Record<TranslationKey, string> {
  const en = translations.en as Record<string, string>;
  const hi = translations.hi as Record<string, string>;
  const patch = (translations as Record<string, Record<string, string>>)[lang] || {};
  const base =
    lang !== 'en' && lang !== 'hi' && INDO_FALLBACK.includes(lang as LangCode)
      ? { ...hi, ...patch }
      : { ...en, ...patch };
  const out = { ...en };
  (Object.keys(en) as TranslationKey[]).forEach((k) => {
    out[k] = base[k] ?? hi[k] ?? en[k] ?? k;
  });
  return out as Record<TranslationKey, string>;
}
